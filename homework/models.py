class WaypointLoss(nn.Module):
    def __init__(self):
        super().__init__()
        self.mse_loss = nn.MSELoss(reduction="none")  # Use 'none' to apply mask later

    def forward(self, pred_waypoints: torch.Tensor, target_waypoints: torch.Tensor,
                waypoints_mask: torch.Tensor) -> torch.Tensor:
        """
        Args:
            pred_waypoints (torch.Tensor): Predicted waypoints with shape (B, n_waypoints, 2)
            target_waypoints (torch.Tensor): Ground truth waypoints with shape (B, n_waypoints, 2)
            waypoints_mask (torch.Tensor): Boolean mask with shape (B, n_waypoints), indicating valid waypoints

        Returns:
            torch.Tensor: Masked MSE loss, averaged over the batch
        """
        # Compute the element-wise squared error for each waypoint
        mse = self.mse_loss(pred_waypoints, target_waypoints)  # Shape: (B, n_waypoints, 2)

        # Sum error over x and y dimensions for each waypoint
        mse_per_waypoint = mse.sum(dim=-1)  # Shape: (B, n_waypoints)

        # Apply mask: only consider valid waypoints in the loss
        masked_mse = mse_per_waypoint * waypoints_mask  # Mask out invalid waypoints

        # Average over the number of valid waypoints
        loss = masked_mse.sum() / waypoints_mask.sum()

        return loss

class ClassificationLoss(nn.Module):
    def forward(self, logits: torch.Tensor, target: torch.LongTensor) -> torch.Tensor:
        """
        Multi-class classification loss

        Args:
            logits: tensor (b, c) logits, where c is the number of classes
            target: tensor (b,) labels

        Returns:
            tensor, scalar loss
        """
        return nn.functional.cross_entropy(logits, target)

class RegressionLoss(nn.Module):
    def forward(self, logits: torch.Tensor, target: torch.LongTensor) -> torch.Tensor:
        """
        Mean Absolute Error (MAE) loss

        Args:
            logits: tensor (b,) predictions (regression outputs)
            target: tensor (b,) ground truth values

        Returns:
            tensor, scalar loss
        """
        return nn.functional.l1_loss(logits, target)
